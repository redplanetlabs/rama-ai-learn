(ns bitmask.private-test-support
  "Shared test logic for the bitmask.private-test-support namespace."
  (:require
   [clojure.test :refer [is testing]]
   [rama-challenges.harness :as harness]))

(defn test-solution
  "Runs the contract tests against a solve function."
  [solve-fn]
  (testing "no plain Clojure functions defined in solution namespace"
    (harness/assert-no-clojure-fns 'bitmask.solution))
  (let [cases [{:input "0\n1\n" :expected "0"}
             {:input "1\n1\n" :expected "1"}
             {:input "?\n1\n" :expected "1"}
             {:input "?01\n6\n" :expected "5"}
             {:input "??1\n6\n" :expected "5"}
             {:input "?1??1??0?1?1??10????????01???0?\n632035333230062517\n" :expected "2139062205"}
             {:input "?????????????????????????????????\n646626281163009046\n" :expected "8589934591"}
             {:input "1100010110101110000011010000000011001101110\n148764707964238687\n" :expected "6792229160558"}
             {:input "010011011?11010010?01??001??0?1?01?11?11?000\n805284105521682493\n" :expected "5357080901624"}
             {:input "?????????????01???????????1??????1?0??????00????0??????1???\n452616349750709279\n" :expected "452576578170158079"}
             {:input "????????????????????????????????????1??????????????????????\n156922299516769673\n" :expected "156922299516190719"}
             {:input "????????????????????????????????????????????????????????????\n1000000000000000000\n" :expected "1000000000000000000"}]]
    (testing "solve"
      (doseq [{:keys [input expected]} cases]
        (testing (str "with input " (pr-str (subs input 0 (min 40 (count input)))))
          (is (= expected (solve-fn input))
              (str "expected " (pr-str expected))))))))
